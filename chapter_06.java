    //что возможно потом понадобится — непозволительная роскошь.
    //Взрослый, в отличие от подростка, обладает жизненным опытом.
    //Он лучше знает свои цели, понимает себя, свои сильные и слабые стороны.
    //Он хочет более индивидуального обучения.


    //Что помогает и мешает при образовании взрослых

    //Обучение и любознательность встроены в нас от рождения. Это хорошая новость.
    //Плохая состоит в том, что предыдущий опыт обучения может нам мешать.

