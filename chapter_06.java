    //Как учатся в школе:
    //•Установленная цель: получить аттестат о получении среднего образования с высокими оценками.

    //•Задачи учителя: передать знания.

    //•Роль учителя: просветитель.

    //•Осознанность в обучении: минимальная —
    //"потому что так надо", "позже, после окончания вуза, тебе это обязательно пригодится"
    //(причем данный срок сравним с текущим возрастом ученика).

    //•Мотивация: в большинстве случаев внешняя — получить хорошую оценку от учителей,
    //быть лучшим в классе, порадовать родителей,
    //сделать домашнее задание и потом заняться более интересными вещами или "чтобы отвязались".

    //•Предыдущий жизненный опыт: в обучении не используется по причине отсутствия.

    //•Самостоятельность: как правило, не приветствуется.

    //•Применение полученных знаний на практике: в средних и старших классах — минимальное.

    //•Обратная связь от учащегося к учителю: только на уровне полученных оценок
    //(если большинство учебной группы не справляется, значит,
    //что-то в образовательном процессе пошло не так).

    //•Обратная связь о карьерных успехах обучавшихся: не собирается и не анализируется.


    //Как учатся взрослые

    //•Установленная цель: достичь конкретных личных целей:
    //получить новую профессию, более интересную работу, повысить доход.

    //•Задачи учителя: сформировать навыки и умения.

    //•Роль учителя: мастер ("делай как я"),  ментор ("подумай и попробуй")
    //или фасилитатор ("обсудите и попробуйте").

    //•Осознанность в обучении: максимальная — "хочу изучать только то, что мне понадобится".

    //•Мотивация: в большинстве случаев — внутренняя: повысить качество своей жизни,
    //удовлетворить потребность в уважении, познании, самоактуализации.

    //•Предыдущий жизненный опыт: активно используется в качестве фундамента
    //для новых знаний и навыков (при качественном обучении), также вспомни про "бесшовный вход".

    //•Самостоятельность: поощряется.

    //•Применение полученных знаний на практике: максимальное, "здесь и сейчас".

    //•Обратная связь от учащегося к учителю: активно собирается и анализируется (при качественном обучении).

    //•Обратная связь о карьерных успехах обучавшихся:
    //активно собирается и анализируется (при качественном обучении).


    //Как можно легко заметить, ни один из пунктов в случае школьного обучения
    //и взрослого обучения не совпадает.


    //Почему так происходит?
    //Причины этого очевидны:
